static inline bool isDoorChar(char c) { return c == '+' || c == 's' || c == 'L'; }

bool hasGlyph(const VaultPrefabDef& def, char glyph) {
    if (!def.rows || def.h <= 0 || def.w <= 0) return false;
    for (int y = 0; y < def.h; ++y) {
        const char* row = def.rows[y];
        if (!row) continue;
        for (int x = 0; x < def.w && row[x] != '\0'; ++x) {
            if (row[x] == glyph) return true;
        }
    }
    return false;
}

int countGlyph(const VaultPrefabDef& def, char glyph) {
    if (!def.rows || def.h <= 0 || def.w <= 0) return 0;
    int n = 0;
    for (int y = 0; y < def.h; ++y) {
        const char* row = def.rows[y];
        if (!row) continue;
        for (int x = 0; x < def.w && row[x] != '\0'; ++x) {
            if (row[x] == glyph) n += 1;
        }
    }
    return n;
}

bool validate(const VaultPrefabDef& def, std::string& outErr) {
    outErr.clear();
    if (!def.name || def.name[0] == '\0') { outErr = "missing name"; return false; }
    if (def.w <= 0 || def.h <= 0) { outErr = "invalid dimensions"; return false; }
    if (!def.rows) { outErr = "missing rows"; return false; }

    int doorCount = 0;
    int doorX = -1;
    int doorY = -1;

    for (int y = 0; y < def.h; ++y) {
        const char* row = def.rows[y];
        if (!row) { outErr = "null row"; return false; }
        const size_t len = std::strlen(row);
        if (static_cast<int>(len) != def.w) {
            outErr = "row length mismatch";
            return false;
        }

        for (int x = 0; x < def.w; ++x) {
            const bool boundary = (x == 0 || y == 0 || x == (def.w - 1) || y == (def.h - 1));
            if (!boundary) continue;

            const char c = row[x];
            if (isDoorChar(c)) {
                // Avoid corner doors (ambiguous outside direction).
                if ((x == 0 || x == (def.w - 1)) && (y == 0 || y == (def.h - 1))) {
                    outErr = "door on corner";
                    return false;
                }
                doorCount += 1;
                doorX = x;
                doorY = y;
            } else if (c != '#') {
                outErr = "non-wall boundary cell";
                return false;
            }
        }
    }

    if (doorCount != 1) { outErr = "invalid door count"; return false; }

    // Ensure the tile inside the entrance is carved (otherwise the door can lead into solid wall).
    if (doorX >= 0 && doorY >= 0) {
        int ix = doorX;
        int iy = doorY;
        if (doorX == 0) ix = 1;
        else if (doorX == (def.w - 1)) ix = def.w - 2;
        else if (doorY == 0) iy = 1;
        else if (doorY == (def.h - 1)) iy = def.h - 2;

        const char inside = def.rows[iy][ix];
        if (inside == '#') {
            outErr = "entrance opens into wall";
            return false;
        }
    }

    return true;
}

